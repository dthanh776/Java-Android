    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        java.sql.Date sqlDate = new java.sql.Date(dtpNgayThu.getDate().getTime());

        ET_PhieuThu et_PhieuThu = new ET_PhieuThu(txtSoPhieuThu.getText(), cbbMaKhachHang.getSelectedItem().toString(), sqlDate, Integer.parseInt(txtSoTien.getText()));
        if (txtSoPhieuThu.getText().compareTo("") == 0 || Integer.parseInt(txtSoTien.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                if (bll_phieuThu.checkTonTaiPhieuThu(txtSoPhieuThu.getText())) {
                    JOptionPane.showMessageDialog(null, "Đã tồn tại số phiếu thu này");
                } else {
                    et_PhieuThu = new ET_PhieuThu(txtSoPhieuThu.getText(), cbbMaKhachHang.getSelectedItem().toString(), sqlDate, Integer.parseInt(txtSoTien.getText()));
                    if (bll_phieuThu.themPhieuThu(et_PhieuThu)) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm không thành công");
                        reset();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                        reset();
                        hienThi();
                        hienThiPhieuThu();
                    }
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        java.sql.Date sqlDate = new java.sql.Date(dtpNgayThu.getDate().getTime());
        ET_PhieuThu et_PhieuThu = new ET_PhieuThu(txtSoPhieuThu.getText(), cbbMaKhachHang.getSelectedItem().toString(), sqlDate, Integer.parseInt(txtSoTien.getText()));

        int row = tblPhieuThu.getSelectedRow();
        if (row >= 0) {
            et_PhieuThu = new ET_PhieuThu(txtSoPhieuThu.getText(), cbbMaKhachHang.getSelectedItem().toString(), sqlDate, Integer.parseInt(txtSoTien.getText()));
            try {
                if (bll_phieuThu.xoaPhieuThu(et_PhieuThu)) {
                    JOptionPane.showMessageDialog(rootPane, "Xoá không thành công");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    try {
                        hienThiPhieuThu();
                        reset();
                    } catch (Exception ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn phiếu thu");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        java.sql.Date sqlDate = new java.sql.Date(dtpNgayThu.getDate().getTime());
        ET_PhieuThu et_PhieuThu = new ET_PhieuThu(txtSoPhieuThu.getText(), cbbMaKhachHang.getSelectedItem().toString(), sqlDate, Integer.parseInt(txtSoTien.getText()));

        if (txtSoPhieuThu.getText().compareTo("") == 0 || Integer.parseInt(txtSoTien.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                bll_phieuThu.suaPhieuThu(et_PhieuThu);
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
                try {
                    hienThiPhieuThu();
                    reset();
                } catch (Exception ex) {
                    Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "Sửa không thành công");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

