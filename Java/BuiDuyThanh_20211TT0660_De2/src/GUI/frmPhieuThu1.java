    private void hienThi() {
        DefaultTableModel model = (DefaultTableModel) tblPhieuThu.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Số phiếu thu");
        model.addColumn("Mã khách hàng");
        model.addColumn("Ngày lập phiếu");
        model.addColumn("Số tiền");
        tblPhieuThu.setModel(model);
    }

    // hiển thị danh mục lên table
    private void hienThiPhieuThu() throws Exception {
        DefaultTableModel model = (DefaultTableModel) tblPhieuThu.getModel();
        model.setRowCount(0);
        ResultSet rs = bll_phieuThu.hienThiPhieuThu();
        while (rs.next()) {
            Object[] col = new Object[4];
            for (int i = 1; i <= 4; i++) {
                col[i - 1] = rs.getObject(i);
            }
            model.addRow(col);
        }
        tblPhieuThu.setModel(model);
        txtSoTien.setText("0");
    }

    public void comboboxMaKhachHang() throws Exception {
        cbbMaKhachHang.removeAllItems();
        ResultSet rs = bll_khachHang.hienThiKhachHang();
        while (rs.next()) {
            cbbMaKhachHang.addItem(rs.getString("MaKH"));
        }
    }

    public void reset() throws Exception {
        txtSoPhieuThu.setText("");
        txtSoTien.setText("0");
        cbbMaKhachHang.setSelectedIndex(0);
        dtpNgayThu.setDate(new Date());
        hienThiPhieuThu();
    }
