    private void btnsapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsapXepActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblPhieuThu.getModel();
        model.setRowCount(0);
        ResultSet rs = null;
        try {
            rs = bll_phieuThu.sapXepPhieuThu();
        } catch (Exception ex) {
            Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while (rs.next()) {
                Object[] col = new Object[4];
                for (int i = 1; i <= 4; i++) {
                    try {
                        col[i - 1] = rs.getObject(i);
                    } catch (SQLException ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                model.addRow(col);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblPhieuThu.setModel(model);
        txtSoTien.setText("0");
    }//GEN-LAST:event_btnsapXepActionPerformed

