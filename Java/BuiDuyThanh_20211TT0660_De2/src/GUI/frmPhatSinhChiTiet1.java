    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        ET_PhatSinhChiTiet et_phatSinhChiTiet = new ET_PhatSinhChiTiet(cbbSoPhieu.getSelectedItem().toString(), cbbMaDichVu.getSelectedItem().toString(), Integer.parseInt(txtSoLuongSuDung.getText()));

        if (Integer.parseInt(txtSoLuongSuDung.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                if (bll_phatSinhChiTiet.themPhatSinhChiTiet(et_phatSinhChiTiet)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm không thành công");
                    try {
                        reset();
                        hienThiPhatSinhChiTiet();
                    } catch (Exception ex) {
                        Logger.getLogger(frmPhatSinhChiTiet1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                    try {
                        hienThiPhatSinhChiTiet();
                        reset();
                    } catch (Exception ex) {
                        Logger.getLogger(frmPhatSinhChiTiet1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        ET_PhatSinhChiTiet et_phatSinhChiTiet = new ET_PhatSinhChiTiet(cbbSoPhieu.getSelectedItem().toString(), cbbMaDichVu.getSelectedItem().toString(), Integer.parseInt(txtSoLuongSuDung.getText()));

        int row = tblPhatSinhChiTiet.getSelectedRow();
        if (row >= 0) {
            try {
                if (bll_phatSinhChiTiet.xoaPhatSinhChiTiet(et_phatSinhChiTiet)) {
                    JOptionPane.showMessageDialog(rootPane, "Xoá không thành công");
                    try {
                        reset();
                    } catch (Exception ex) {
                        Logger.getLogger(frmPhatSinhChiTiet1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    try {
                        hienThiPhatSinhChiTiet();
                    } catch (Exception ex) {
                        Logger.getLogger(frmPhatSinhChiTiet1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    try {
                        hienThiPhatSinhChiTiet();
                        reset();
                        hienThiPhatSinhChiTiet();
                    } catch (Exception ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

