    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        ET_PhatSinhChiTiet et_phatSinhChiTiet = new ET_PhatSinhChiTiet(cbbSoPhieu.getSelectedItem().toString(), cbbMaDichVu.getSelectedItem().toString(), Integer.parseInt(txtSoLuongSuDung.getText()));

        if (Integer.parseInt(txtSoLuongSuDung.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                bll_phatSinhChiTiet.suaPhatSinhChiTiet(et_phatSinhChiTiet);
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
                try {
                    hienThiPhatSinhChiTiet();
                    reset();
                } catch (Exception ex) {
                    Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "Sửa không thành công");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void txtSoLuongSuDungKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSoLuongSuDungKeyPressed
        char gia = evt.getKeyChar();
        if (Character.isLetter(gia)) {
            txtSoLuongSuDung.setEditable(false);
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số");
        } else {
            txtSoLuongSuDung.setEditable(true);
        }
    }//GEN-LAST:event_txtSoLuongSuDungKeyPressed
    private void hienThi() {
        DefaultTableModel model = (DefaultTableModel) tblPhatSinhChiTiet.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        model.addColumn("Số phiếu");
        model.addColumn("Mã dịch vụ");
        model.addColumn("Số lượng sử dụng");
        tblPhatSinhChiTiet.setModel(model);
    }

    private void hienThiPhatSinhChiTiet() throws Exception {
        DefaultTableModel model = (DefaultTableModel) tblPhatSinhChiTiet.getModel();
        model.setRowCount(0);
        ResultSet rs = bll_phatSinhChiTiet.hienThiPhatSinhChiTiet();
        while (rs.next()) {
            Object[] col = new Object[3];
            for (int i = 1; i <= 3; i++) {
                col[i - 1] = rs.getObject(i);
            }
            model.addRow(col);
        }
        tblPhatSinhChiTiet.setModel(model);
        txtSoLuongSuDung.setText("0");
    }

    public void comboboxSoPhieu() throws Exception {
        cbbSoPhieu.removeAllItems();
        ResultSet rs = bll_phatSinh.hienThiPhatSinh();
        while (rs.next()) {
            cbbSoPhieu.addItem(rs.getString("SoPhieu"));
        }
    }

    public void comboboxMaDichVu() throws Exception {
        cbbMaDichVu.removeAllItems();
        ResultSet rs = bll_dichVu.hienThiDichVu();
        while (rs.next()) {
            cbbMaDichVu.addItem(rs.getString("MaDV"));
        }
    }

    public void reset() throws Exception {
        cbbMaDichVu.setSelectedIndex(0);
        cbbSoPhieu.setSelectedIndex(0);
        txtSoLuongSuDung.setText("0");
    }

