    private void txtGiaDichVuKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGiaDichVuKeyPressed
        char gia = evt.getKeyChar();
        if (Character.isLetter(gia)) {
            txtGiaDichVu.setEditable(false);
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số");
        } else {
            txtGiaDichVu.setEditable(true);
        }
    }//GEN-LAST:event_txtGiaDichVuKeyPressed

    private void btnsapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsapXepActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
        model.setRowCount(0);
        ResultSet rs = null;
        try {
            rs = bll_dichVu.sapXep();
        } catch (Exception ex) {
            Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while (rs.next()) {
                Object[] col = new Object[4];
                for (int i = 1; i <= 4; i++) {
                    try {
                        col[i - 1] = rs.getObject(i);
                    } catch (SQLException ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                model.addRow(col);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblDichVu.setModel(model);
        txtGiaDichVu.setText("0");

    }//GEN-LAST:event_btnsapXepActionPerformed

