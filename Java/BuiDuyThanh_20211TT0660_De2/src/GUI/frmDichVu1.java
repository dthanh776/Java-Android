    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        ET_DichVu et_DichVu = new ET_DichVu(txtMaDichVu.getText(), txtTenDichVu.getText(), txtDonViTinh.getText(), Integer.parseInt(txtGiaDichVu.getText()));

        if (txtMaDichVu.getText().compareTo("") == 0 || txtTenDichVu.getText().compareTo("") == 0 || txtDonViTinh.getText().compareTo("") == 0 || Integer.parseInt(txtGiaDichVu.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                if (bll_dichVu.checkTonTaiDichVu(txtMaDichVu.getText())) {
                    JOptionPane.showMessageDialog(null, "Đã tồn tại mã dịch vụ này");
                } else {
                    if (bll_dichVu.themDichVu(et_DichVu)) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm không thành công");
                        reset();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                        reset();
                        hienThi();
                        hienThiDichVu();
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        ET_DichVu et_DichVu = new ET_DichVu(txtMaDichVu.getText(), txtTenDichVu.getText(), txtDonViTinh.getText(), Integer.parseInt(txtGiaDichVu.getText()));

        int row = tblDichVu.getSelectedRow();
        if (row >= 0) {
            et_DichVu = new ET_DichVu(txtMaDichVu.getText(), txtTenDichVu.getText(), txtDonViTinh.getText(), Integer.parseInt(txtGiaDichVu.getText()));
            try {
                if (bll_dichVu.xoaDichVu(et_DichVu)) {
                    JOptionPane.showMessageDialog(rootPane, "Xoá không thành công");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    try {
                        hienThiDichVu();
                        reset();
                    } catch (Exception ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn dịch vụ");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        ET_DichVu et_DichVu = new ET_DichVu(txtMaDichVu.getText(), txtTenDichVu.getText(), txtDonViTinh.getText(), Integer.parseInt(txtGiaDichVu.getText()));
        int giaDichVu = Integer.parseInt(txtGiaDichVu.getText());
        if (txtMaDichVu.getText().compareTo("") == 0 || txtTenDichVu.getText().compareTo("") == 0 || txtDonViTinh.getText().compareTo("") == 0 || giaDichVu <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                bll_dichVu.suaDichVu(et_DichVu);
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
                try {
                    hienThiDichVu();
                    reset();
                } catch (Exception ex) {
                    Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "Sửa không thành công");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

