    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        java.sql.Date sqlDate = new java.sql.Date(dtpNgaySinh.getDate().getTime());

        ET_KhacHang et_khachHang = new ET_KhacHang(txtMaKhachHang.getText(), txtTenKhachHang.getText(), sqlDate, txtDiaChi.getText());

        if (txtMaKhachHang.getText().compareTo("") == 0 || txtTenKhachHang.getText().compareTo("") == 0 || txtDiaChi.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
        } else {
            try {
                if (bll_khachHang.checkTonTaiKhachHang(txtMaKhachHang.getText())) {
                    JOptionPane.showMessageDialog(null, "Đã tồn tại mã khách hàng");
                } else {
                    et_khachHang = new ET_KhacHang(txtMaKhachHang.getText(), txtTenKhachHang.getText(), sqlDate, txtDiaChi.getText());
                    if (bll_khachHang.themKhachHang(et_khachHang)) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm không thành công");
                        reset();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                        reset();
                        hienThi();
                        hienThiKhachHang();
                    }
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        java.sql.Date sqlDate = new java.sql.Date(dtpNgaySinh.getDate().getTime());
        ET_KhacHang et_khachHang = new ET_KhacHang(txtMaKhachHang.getText(), txtTenKhachHang.getText(), sqlDate, txtDiaChi.getText());

        int row = tblKhachHang.getSelectedRow();
        if (row >= 0) {
            et_khachHang = new ET_KhacHang(txtMaKhachHang.getText(), txtTenKhachHang.getText(), sqlDate, txtDiaChi.getText());
            try {
                if (bll_khachHang.xoaKhachHang(et_khachHang)) {
                    JOptionPane.showMessageDialog(rootPane, "Xoá không thành công");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    try {
                        hienThiKhachHang();
                        reset();
                    } catch (Exception ex) {
                        Logger.getLogger(frmDichVu1.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn khách hàng");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

