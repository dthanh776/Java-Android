    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int row = tblKhachHang.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        txtMaKhachHang.setText(model.getValueAt(row, 0).toString());
        txtTenKhachHang.setText(model.getValueAt(row, 1).toString());
        String date = model.getValueAt(row, 2).toString();
        try {
            dtpNgaySinh.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(date));
        } catch (ParseException ex) {
            Logger.getLogger(frmKhachHang1.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtDiaChi.setText(model.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblKhachHangMouseClicked

